                            <h3>Selmer</h3>
                            <p>The Selmer guitar — often called a Selmer-Maccaferri or just Maccaferri by English speakers, as early British advertising stressed the designer rather than manufacturer — is an unusual acoustic guitar best known as the favored instrument of Django Reinhardt. Selmer, a French manufacturer, produced the instrument from 1932 to about 1952.</p>

                            <p>
                                Reconstruction of a portion of the Selmer-Maccaferri guitar workshop (1933), Musée de la Musique exhibition, 2012
                                In 1932 Selmer partnered with the Italian guitarist and luthier Mario Maccaferri to produce a line of acoustic guitars based on Maccaferri's unorthodox design.[1] Although Maccaferri's association with Selmer ended in 1934, the company continued to make several models of this guitar until 1952. The guitar was closely associated with jazz guitarist Django Reinhardt.
                            </p>

                            <p>
                                Construction
                                In its archetypal steel-string Jazz/Orchestre form, the Selmer is distinguished by a fairly large body with squarish bouts, either a "D"-shaped or longitudinal oval sound hole, and a cutaway in the upper right bout. The strings pass over a movable bridge and are gathered at the tail, as on a mandolin. Two "moustache" markers are fixed to the soundboard to help position the movable bridge. The top of the guitar is gently arched or domed — a feature achieved by bending a flat piece of wood rather than by the violin-style carving used in archtop guitars. The top is also rather thin, at about 2 mm (0.079 in). It has a comparatively wide fretboard (about 47 mm or 1.850 in at the nut) and a snake-shaped, slotted headstock. The back and top are both ladder-braced, which was the norm for French and Italian steel-string guitars of the time (unlike American guitars, which frequently employed X-braced tops by this period).
                            </p>

                            <p>Other models can be more conventional in appearance and construction, with the Modèle Classique, for example, essentially being a standard fan-braced, flat-top classical guitar.</p>
