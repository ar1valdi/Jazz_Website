                            <h3>Hollow Body</h3>
                            <p>
                                Typ budowy gitar akustycznych oraz elektryczno-akustycznych charakteryzujący się występowaniem grubego pudła rezonansowego oraz otworami rezonansowymi w kształcie stylizowanej litery f. Mają niższe boki, a spód z wierzchnią płytą są wypukłe. Mostek może nie być przymocowany do pudła, a przytrzymywany jedynie siłą nacisku strun. W modelach przeznaczonych do rock'n'rolla zdarzają się czasem mostki typu Bigsby.
                            </p>
                            <p>
                                Instrumenty typu Arch Top są najczęściej skonstruowane z następujących gatunków drewna: płyta tylna – jawor, klon lub mahoń w tańszych instrumentach
                                boczki – jawor, klon lub mahoń w tańszych instrumentach
                                płyta wierzchnia, tzw. top – jawor, świerk, jawor o falistym układzie włókien, klon o falistym układzie włókien
                                W tanich instrumentach zamiast litego drewna stosuje się sklejkę.
                            </p>
                            <p>
                                Pudła są najczęściej przyozdobione jedną lub dwiema żyłkami po bokach, tzw. binding. Chętnie używane przez muzyków bluesowych, jazzowych i rockandrollowych (np. B.B. Kinga – gitara bez otworów w kształcie litery „f”, tzw. „efek”), Chucka Berry'ego, czy Malcolma Younga)
