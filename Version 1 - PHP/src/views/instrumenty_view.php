                            <h3>Archtop</h3>
                            <p>
                                The archtop guitar is often credited to Orville Gibson, whose innovative designs led to the formation of the Gibson Mandolin-Guitar Mfg. Co, Ltd in 1902. His 1898 patent for a mandolin, which was also applicable to guitars according to the specifications, was intended to enhance "power and quality of tone."
                            </p>
                            <p>
                                Among the features of this instrument were a violin-style arched top and back, each carved from a single piece of wood, and thicker in the middle than at the sides; sides carved to shape from a single block of wood; and a lack of internal "braces, splices, blocks, or bridges . . . which, if employed, would rob the instrument of much of its volume of tone." 
                            </p><p>                            But Gibson was not the first to apply violin design principles to the guitar. Guitar maker A. H. Merrill, for example, patented in 1896 a very modern looking instrument "of the guitar and mandolin type . . . with egg-shaped hoop or sides and a graduated convex back and top." The instrument featured a metal tailpiece and teardrop shaped "f-holes," and strongly resembled the archtop guitars of the 1930s.</p>
